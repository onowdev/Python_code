# dictionary Data type
'''
Dictionary consists of key-value pairs. 
It is enclosed by curly braces {} and values can be assigned and accessed
using square brackets[]
'''

typeDictionary = {'name':'MightyEagle' , 'age':20}

print(typeDictionary)
print(typeDictionary['name'])
print(typeDictionary.values())
print(typeDictionary.keys())
