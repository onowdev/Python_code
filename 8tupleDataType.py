# Tuple Data Type
'''
Lists are enclosed in brackets [ ] and their elements and size can be changed, while tuples are enclosed in
parentheses ( ) and cannot be updated. Tuples are immutable.
'''

tuple_a = (123,'hello')
tuple_b = ('world')

print(tuple_a)
print(tuple_b)
print(tuple_a[1])
print(tuple_b[0:4])
